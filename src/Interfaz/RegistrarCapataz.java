    private int datosValidos(String nombre, String cedula, String direccion, int anio) {
        int error = 0;
        if (nombre.isEmpty() || cedula.isEmpty() || direccion.isEmpty()) {
            error = 1;
        } else {
            try {
                anio = Integer.parseInt(txtAnioCapataz.getText());
            } catch (NumberFormatException e) {
                error = 2;
            }
        }
        return error;
    }
