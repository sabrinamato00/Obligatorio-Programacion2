    private void btnRegistrarCapatazActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarCapatazActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombreCapataz.getText();
        String cedula = txtCedulaCapataz.getText();
        String direccion = txtDireccionCapataz.getText();
        int anio = 0;

        if (datosValidos(nombre, cedula, direccion, anio) == 1) {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            if (datosValidos(nombre, cedula, direccion, anio) == 2) {
                JOptionPane.showMessageDialog(null, "El año es invalido", "Error", JOptionPane.WARNING_MESSAGE);
            } else {
                if (sistema.existeCapataz(cedula)) {
                    JOptionPane.showMessageDialog(this, "El capataz ya existe", "", JOptionPane.PLAIN_MESSAGE);
                } else {
                    anio = Integer.parseInt(txtAnioCapataz.getText());
                    Capataz unCapataz = new Capataz(nombre, cedula, direccion, anio);
                    sistema.agregarCapataz(unCapataz);
                    JOptionPane.showMessageDialog(this, "Capataz registrado correctamente", "", JOptionPane.PLAIN_MESSAGE);
                    //System.out.println(unCapataz);
                }
            }
            objetoAPantalla();

        }

    }//GEN-LAST:event_btnRegistrarCapatazActionPerformed

